  <h2>You are not on the internet</h2>
  <p>You have connected to a wifi network that leads only to this website. If you're looking for the internet, disconnect from the hello.box network and look elsewhere.</p>
  
  <h2>Hello, box</h2>
  <p>This is <strong>hello.box</strong>, a small, portable, personal backchannel. It's a wifi router carried around by someone wherever they go. They can show you stuff, and you can leave them messages, and talk to other people who are also nearby. That's it.</p>
  <p>hello.box was developed by James Bridle. Find out more at <strong>http://booktwo.org</strong>. (No, that's not a link. You'll need the internet to do that!)</p>
  
  <h2>Thanks</h2>
  <p>hello.box is based on <strong>occupy.here</strong>, a free, anonymous, local forum inspired by Occupy Wall Street and developed by Dan Phiffer. Find out more at <strong>http://occupyhere.org</strong> (Still not a link).</p>
  <p>hello.box was also inspired by Aram Bartholl's OFFLINE ART project, showing offline net art projects through dedicated wifi points in a gallery space. See <strong>http://www.datenform.de</strong> for more info.</p>
  
